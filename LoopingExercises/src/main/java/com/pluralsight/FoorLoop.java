package com.pluralsight;

public class FoorLoop {
    public static void main(String[] args) {
        // create a for loop to display countdown from 10 -1 and ends with the word launch
        for (int i = 10; i > 0; i--) { // int i = start value , i<10 = condition, i++ = what increment by)
            System.out.println(i);

        }
    }
}
